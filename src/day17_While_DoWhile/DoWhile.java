package day17_While_DoWhile;

public class DoWhile {
    public static void main(String[] args) {
        int i = 0;

        do{
            System.out.println("Hello");
        }while(i > 5);

        System.out.println("-------");

        while(i >5){
            System.out.println("Hello");
        }

        System.out.println("-------");



    }}

