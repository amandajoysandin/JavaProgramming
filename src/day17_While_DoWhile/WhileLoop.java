package day17_While_DoWhile;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1; //you have to declare the variable outside of the while loop

        while(i<=10) {

            System.out.println(i);
            i++; //you have to increase each time or you have an infinite loop
//if you add the iterator (i++) before the print statement it will add before printing
        }



    }
}
