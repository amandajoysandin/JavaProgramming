package day04_Variables;

public class Currencies {

    public static void main(String[] args) {
        int dollar = 1000;

        double lira = dollar * 9.53;
        double euro = dollar * 1.86;
        double jpy = dollar * .0088;
        double peso = dollar * .048;
        double tunDinar = dollar * 2.81;
        double quetzal = dollar * 7.73;
        double cad = dollar * 1.24;
        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("jpy = " + jpy);
        System.out.println("peso = " + peso);
        System.out.println("tunDinar = " + tunDinar);
        System.out.println("quetzal = " + quetzal);
        System.out.println("cad = " + cad);




    }
}
